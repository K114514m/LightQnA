def normalize_language(language: str | None) -> str:
    """Return a supported language code."""
    if language in LANGUAGES:
        return str(language)
    return DEFAULT_LANGUAGE


def t(language: str | None, key: str, **kwargs: object) -> str:
    """Translate a UI key, falling back to Chinese and then the key itself."""
    language = normalize_language(language)
    template = TEXT.get(language, {}).get(key, TEXT[DEFAULT_LANGUAGE].get(key, key))
    return template.format(**kwargs)
